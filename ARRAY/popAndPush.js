//Remove the last element in an array
const fruits = ["Banana", "Orange", "Apple", "Mango"];
fruits.pop();
console.log(fruits); //["Banana", "Orange", "Apple"]

//Add an element to the array (at the end)
const Fruits = ["Banana", "Orange", "Apple", "Mango"];
Fruits.push("addedMelon");
console.log(Fruits)// ["Banana", "Orange", "Apple", "Mango", "addedMelon"]