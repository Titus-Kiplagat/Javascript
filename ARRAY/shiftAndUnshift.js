//shift()
const fruits = ["Banana", "Orange", "Apple", "Mango"];
fruits.shift();
console.log(fruits);//["Orange", "Apple", "Mango"]

//unshift()
const Fruits = ["Banana", "Orange", "Apple", "Mango"];
Fruits.unshift("addedLemon");
console.log(Fruits);//["addedLemon", "Banana", "Orange", "Apple", "Mango"]